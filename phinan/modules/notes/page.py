def notes_content() -> rx.Component:
    """Notes page content."""
    return rx.vstack(
        rx.heading("Structured Notes Analyzer", size="6"),
        rx.text(
            "Decompose opaque bank products to reveal actual fees, risks, and opportunity costs.",
            size="2",
            color_scheme="gray",
        ),
        rx.divider(),
        
        # Upload Section (Always visible if no results, otherwise collapsed or side-by-side?)
        # Let's keep it simple: Upload top, results below.
        rx.cond(
            ~NoteState.has_results,
            upload_zone(),
            rx.vstack(
                rx.hstack(
                    rx.heading(NoteState.note_summary, size="5"),
                    rx.spacer(),
                    rx.button("Analyze Another", variant="outline", on_click=NoteState.clear_analysis),
                    width="100%",
                ),
                valuation_card(),
                note_details_card(),
                spacing="4",
                width="100%",
            ),
        ),
        
        spacing="4",
        width="100%",
        max_width="800px",
    )


@rx.page(
    route="/notes",
    title="Notes | Phinan Finance Suite",
    on_load=[UserContextState.load_context, PortfolioState.load_positions],
)
def notes_page() -> rx.Component:
    """Notes page."""
    return main_layout(notes_content())
