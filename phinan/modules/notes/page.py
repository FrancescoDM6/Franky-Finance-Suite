"""Notes page - Structured note analysis.

Decompose opaque bank products to reveal actual fees, risks, and opportunity costs.
"""

import reflex as rx

from ...components.layout import main_layout
from ...state.user_context import UserContextState
from ..portfolio.state import PortfolioState
from .state import NoteState

def upload_zone() -> rx.Component:
    """File upload zone."""
    return rx.vstack(
        rx.upload(
            rx.vstack(
                rx.icon("file-up", size=30, color="var(--accent-9)"),
                rx.text(
                    "Drop Term Sheet PDF here",
                    weight="bold",
                    size="4",
                ),
                rx.text(
                    "Supports typical Autocallable/Reverse Convertible term sheets",
                    size="2",
                    color_scheme="gray",
                ),
                spacing="2",
                align="center",
            ),
            id="note_upload",
            multiple=False,
            accept={"application/pdf": [".pdf"]},
            max_files=1,
            border="2px dashed var(--accent-a6)",
            background="var(--accent-a2)",
            padding="2em",
            border_radius="1em",
            width="100%",
            height="200px",
            class_name="shark-hover",
        ),
        rx.cond(
            NoteState.is_uploading,
            rx.vstack(
                rx.text(NoteState.parsing_status, size="2"),
                rx.progress(value=NoteState.upload_progress, width="100%"),
                spacing="2",
                width="100%",
            ),
        ),
        rx.button(
            "Analyze Note",
            on_click=NoteState.handle_upload(rx.upload_files(upload_id="note_upload")),
            loading=NoteState.is_uploading,
            disabled=NoteState.is_uploading,
            width="100%",
        ),
        rx.cond(
            NoteState.error_message != "",
            rx.callout(
                NoteState.error_message,
                icon="alert-triangle",
                color_scheme="red",
                width="100%",
            ),
        ),
        spacing="4",
        width="100%",
    )

